static void emulator_command(SDL_Keycode sym) {
	switch (sym) {
	case SDL_SCANCODE_1: case SDL_SCANCODE_2: case SDL_SCANCODE_3: case SDL_SCANCODE_4:
		if (shift) {
			xroar_new_disk(sym - SDL_SCANCODE_1);
		} else {
			xroar_insert_disk(sym - SDL_SCANCODE_1);
		}
		break;
	case SDL_SCANCODE_5: case SDL_SCANCODE_6: case SDL_SCANCODE_7: case SDL_SCANCODE_8:
		if (shift) {
			xroar_set_write_back(1, sym - SDL_SCANCODE_5, XROAR_TOGGLE);
		} else {
			xroar_set_write_enable(1, sym - SDL_SCANCODE_5, XROAR_TOGGLE);
		}
		break;
	case SDLK_a:
		xroar_set_cross_colour(1, XROAR_CYCLE);
		break;
	case SDLK_c:
	case SDLK_q:
		xroar_quit();
		break;
	case SDLK_e:
		xroar_toggle_cart();
		break;
	case SDLK_f:
		xroar_set_fullscreen(1, XROAR_TOGGLE);
		break;
	case SDLK_h:
		if (shift)
			machine_toggle_pause();
		break;
	case SDLK_i:
		if (shift)
			xroar_set_vdg_inverted_text(1, XROAR_TOGGLE);
		else
			xroar_run_file(NULL);
		break;
	case SDLK_j:
		if (shift) {
			xroar_swap_joysticks(1);
		} else {
			xroar_cycle_joysticks(1);
		}
		break;
	case SDLK_k:
		xroar_set_keymap(1, XROAR_CYCLE);
		break;
	case SDLK_l:
		if (shift) {
			xroar_run_file(NULL);
		} else {
			xroar_load_file(NULL);
		}
		break;
	case SDLK_m:
		xroar_set_machine(1, XROAR_CYCLE);
		break;
	case SDLK_p:
		if (shift)
			printer_flush();
		break;
	case SDLK_r:
		if (shift)
			xroar_hard_reset();
		else
			xroar_soft_reset();
		break;
	case SDLK_s:
		xroar_save_snapshot();
		break;
	case SDLK_w:
		xroar_select_tape_output();
		break;
#ifdef TRACE
	case SDLK_v:
		xroar_set_trace(XROAR_TOGGLE);
		break;
#endif
	case SDLK_z: /* running out of letters... */
		xroar_set_kbd_translate(1, XROAR_TOGGLE);
		break;
	case SDLK_EQUALS:
		sdl_zoom_in();
		break;
	case SDLK_MINUS:
		sdl_zoom_out();
		break;
	default:
		break;
	}
	return;
}

void sdl_keypress(SDL_Keysym *keysym) {
	SDL_Scancode scancode = keysym->scancode;
	SDL_Keycode sym = keysym->sym;
	if (scancode == SDL_SCANCODE_UNKNOWN)
		return;
	if (scancode >= SDL_NUM_SCANCODES)
		return;
	if (sym == SDL_SCANCODE_UNKNOWN)
		return;

	for (unsigned i = 0; i < MAX_AXES; i++) {
		if (enabled_axis[i]) {
			if (sym == enabled_axis[i]->key0) {
				enabled_axis[i]->value = 0;
				return;
			}
			if (sym == enabled_axis[i]->key1) {
				enabled_axis[i]->value = 255;
				return;
			}
		}
	}
	for (unsigned i = 0; i < MAX_BUTTONS; i++) {
		if (enabled_button[i]) {
			if (sym == enabled_button[i]->key) {
				enabled_button[i]->value = 1;
				return;
			}
		}
	}

	shift = keysym->mod & KMOD_SHIFT;
	control = keysym->mod & KMOD_CTRL;

	if (sym == SDLK_LSHIFT || sym == SDLK_RSHIFT) {
		KEYBOARD_PRESS_SHIFT;
		return;
	}
	if (sym == SDLK_LCTRL || sym == SDLK_RCTRL) {
		return;
	}
	if (sym == SDLK_F11) {
		xroar_set_fullscreen(1, XROAR_TOGGLE);
		return;
	}
	if (sym == SDLK_F12) {
		if (shift) {
			noratelimit_latch = !noratelimit_latch;
			if (noratelimit_latch) {
				xroar_noratelimit = 1;
				xroar_frameskip = 10;
			} else {
				xroar_noratelimit = 0;
				xroar_frameskip = xroar_cfg.frameskip;
			}
		} else if (!noratelimit_latch) {
			xroar_noratelimit = 1;
			xroar_frameskip = 10;
		}
		return;
	}
	if (sym == SDLK_PAUSE) {
		machine_toggle_pause();
		return;
	}
	if (control) {
		emulator_command(sym);
		return;
	}

	if (scancode_priority[scancode]) {
		if (xroar_cfg.debug_ui & XROAR_DEBUG_UI_KBD_EVENT)
			printf("sdl press   scan %6d   key %6d   %s\n", scancode, sym, SDL_GetKeyName(sym));
		keyboard_press(scancode_to_dkey[scancode]);
		return;
	}

	//if (xroar_cfg.kbd_translate) {
	//	unsigned unicode;
	//	unicode = keysym->unicode;
	//	if (xroar_cfg.debug_ui & XROAR_DEBUG_UI_KBD_EVENT)
	//		printf("sdl press   scan %6d   key %6d   unicode %08x   %s\n", scancode, sym, unicode, SDL_GetKeyName(sym));
	//	/* shift + backspace -> erase line */
	//	if (shift && (unicode == 0x08 || unicode == 0x7f))
	//		unicode = DKBD_U_ERASE_LINE;
	//	/* shift + enter -> caps lock */
	//	if (scancode_to_dkey[scancode] == DSCAN_ENTER)
	//		unicode = shift ? DKBD_U_CAPS_LOCK : 0x0d;
	//	/* shift + clear -> pause output */
	//	if (scancode_to_dkey[scancode] == DSCAN_SPACE)
	//		unicode = shift ? DKBD_U_PAUSE_OUTPUT : 0x20;
	//	unicode_last_scancode[scancode] = unicode;
	//	keyboard_unicode_press(unicode);
	//	return;
	//}

	int8_t dkey = scancode_to_dkey[scancode];
	if (xroar_cfg.debug_ui & XROAR_DEBUG_UI_KBD_EVENT)
		printf("sdl press   scan %6d   key %6d   dkey %6d   %s\n", scancode, sym, dkey, SDL_GetKeyName(sym));
	keyboard_press(dkey);
}

void sdl_keyrelease(SDL_Keysym *keysym) {
	SDL_Scancode scancode = keysym->scancode;
	SDL_Keycode sym = keysym->sym;
	if (scancode == SDL_SCANCODE_UNKNOWN)
		return;
	if (scancode >= SDL_NUM_SCANCODES)
		return;
	if (sym == SDL_SCANCODE_UNKNOWN)
		return;

	for (unsigned i = 0; i < MAX_AXES; i++) {
		if (enabled_axis[i]) {
			if (sym == enabled_axis[i]->key0) {
				if (enabled_axis[i]->value < 129)
					enabled_axis[i]->value = 129;
				return;
			}
			if (sym == enabled_axis[i]->key1) {
				if (enabled_axis[i]->value > 130)
					enabled_axis[i]->value = 130;
				return;
			}
		}
	}
	for (unsigned i = 0; i < MAX_BUTTONS; i++) {
		if (enabled_button[i]) {
			if (sym == enabled_button[i]->key) {
				enabled_button[i]->value = 0;
				return;
			}
		}
	}

	shift = keysym->mod & KMOD_SHIFT;
	control = keysym->mod & KMOD_CTRL;

	if (sym == SDLK_LSHIFT || sym == SDLK_RSHIFT) {
		KEYBOARD_RELEASE_SHIFT;
		return;
	}
	if (sym == SDLK_LCTRL || sym == SDLK_RCTRL) { return; }
	if (sym == SDLK_F12) {
		if (!noratelimit_latch) {
			xroar_noratelimit = 0;
			xroar_frameskip = xroar_cfg.frameskip;
		}
		return;
	}

	if (scancode_priority[scancode]) {
		if (xroar_cfg.debug_ui & XROAR_DEBUG_UI_KBD_EVENT)
			printf("sdl release scan %6d   key %6d\n", scancode, sym);
		keyboard_release(scancode_to_dkey[scancode]);
		return;
	}

	//if (xroar_cfg.kbd_translate) {
	//	unsigned unicode;
	//	if (scancode >= SDL_NUM_SCANCODES)
	//		return;
	//	unicode = unicode_last_scancode[scancode];
	//	if (xroar_cfg.debug_ui & XROAR_DEBUG_UI_KBD_EVENT)
	//		printf("sdl release code %6d   key %6d   unicode %08x   %s\n", scancode, sym, unicode, SDL_GetKeyName(sym));
	//	keyboard_unicode_release(unicode);
	//	/* Put shift back the way it should be */
	//	if (shift)
	//		KEYBOARD_PRESS_SHIFT;
	//	else
	//		KEYBOARD_RELEASE_SHIFT;
	//	return;
	//}

	if (xroar_cfg.debug_ui & XROAR_DEBUG_UI_KBD_EVENT)
		printf("sdl release scan %6d   key %6d\n", scancode, sym);
	int8_t dkey = scancode_to_dkey[scancode];
	keyboard_release(dkey);
}

