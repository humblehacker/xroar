static void map_keyboard(struct keymap *keymap) {
	/* First clear the table and map obvious keys */
	for (int i = 0; i < SDL_NUM_SCANCODES; i++) {
		scancode_to_dkey[i] = DSCAN_INVALID;
		scancode_priority[i] = 0;
	}
	for (unsigned i = 0; i < ARRAY_N_ELEMENTS(scancode_dkey_default); i++) {
		scancode_to_dkey[scancode_dkey_default[i].scancode] = scancode_dkey_default[i].dkey;
		scancode_priority[scancode_dkey_default[i].scancode] = scancode_dkey_default[i].priority;
	}
	for (int i = 0; i < 9; i++) {
		scancode_to_dkey[SDL_SCANCODE_1 + i] = DSCAN_1 + i;
		scancode_to_dkey[SDL_SCANCODE_KP_1 + i] = DSCAN_1 + i;
	}
	scancode_to_dkey[SDL_SCANCODE_0] = DSCAN_0;
	scancode_to_dkey[SDL_SCANCODE_KP_0] = DSCAN_0;
	for (int i = 0; i <= 25; i++)
		scancode_to_dkey[SDL_SCANCODE_A + i] = DSCAN_A + i;

	for (int i = 0; i < SDL_NUM_SCANCODES; i++)
		unicode_last_scancode[i] = 0;
	if (keymap == NULL)
		return;
	int num_mappings = keymap->num_mappings;
	struct scancode_dkey_mapping *mappings = keymap->mappings;
	if (!mappings)
		return;
	for (int i = 0; i < num_mappings; i++) {
		scancode_to_dkey[mappings[i].scancode] = mappings[i].dkey;
		scancode_priority[mappings[i].scancode] = mappings[i].priority;
	}
}

void sdl_keyboard_init(void) {
	const char *keymap_option = xroar_cfg.keymap;
	struct keymap *selected_keymap = &keymaps[0];
	if (keymap_option) {
		if (0 == strcmp(keymap_option, "help")) {
			for (unsigned i = 0; i < ARRAY_N_ELEMENTS(keymaps); i++) {
				if (keymaps[i].description)
					printf("\t%-10s %s\n", keymaps[i].name, keymaps[i].description);
			}
			exit(EXIT_SUCCESS);
		}
		for (unsigned i = 0; i < ARRAY_N_ELEMENTS(keymaps); i++) {
			if (0 == strcmp(keymap_option, keymaps[i].name)) {
				selected_keymap = &keymaps[i];
				LOG_DEBUG(1, "\tSelecting '%s' keymap\n",keymap_option);
			}
		}
	}
	map_keyboard(selected_keymap);
	//SDL_EnableUNICODE(xroar_cfg.kbd_translate);

	for (unsigned i = 0; i < MAX_AXES; i++)
		enabled_axis[i] = NULL;
	for (unsigned i = 0; i < MAX_BUTTONS; i++)
		enabled_button[i] = NULL;
}

